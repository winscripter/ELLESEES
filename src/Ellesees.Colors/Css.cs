    public static RgbColor FromHexLowercase(string text)
    {
        text = text.ToLowerInvariant();

        return LowercaseHexToRgb[text];
    }

    public static RgbColor FromCssLowercase(string text)
    {
        text = text.ToLowerInvariant();

        return LowercaseCssToRgb[text];
    }

    public static string HexOrCss(string text)
    {
        if (text[0] == '#')
        {
            return text;
        }
        else
        {
            return CssLowercaseToHex[text.ToLower()];
        }
    }
